# Implement a program with functions, for finding the area of circle, triangle, square.

def circle(r):
    return 3.14*r*r
print("area of circle",circle(3))


def triangle(b,h):
    return 0.5*b*h
print("area of triangle",triangle(3,4))

def square(l):
    return l*l
print("area of square",square(3))